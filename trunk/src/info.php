<H3 align="center" class="Design"><b>Wondering what PolyGame is?</b></H3>
<div align="center" class="Design">
<b>Polygame</b> aims at making the players aware of the climate crisis and try to engage them through a game which makes them ponder about possible solution to the excess of CO2 emissions.<BR>
The goal is not only to make players more aware of the climate crisis but also provide them with a broader knowledge of CO2 emission solutions. We really hope that this will lead players to change their everyday behavior due to their deeper understanding of environmental issues.<BR><BR>
</div>
